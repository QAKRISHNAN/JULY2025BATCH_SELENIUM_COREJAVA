package Operators;

public class Relational_Operators 
{

	public static void main(String[] args) 
	{
		int a=5,b=7;
		
		System.out.println(a<b); //5<7 true 
		System.out.println(a<=b);//5<=7 true
		
		System.out.println(a>b); //5>7 false
		System.out.println(a>=b);//5>=7 false  
		
		System.out.println(a==b);//5==7 false 
		System.out.println(a!=b);//5!=7 true 

	}

}
